async function testMap() {
    const res = await fetch('http://localhost:5000/api/products');
    const json = await res.json();
    try {
        const allProducts = json.data.map(p => {
            let foundMake = 'Toyota';
            for(let make in vehicleDB) {
                if(p.compatibility.some(m => vehicleDB[make][m])) { foundMake = make; break; }
            }
            const catIcons = {'Suspension': '🔩', 'Filters': '💨', 'Body Parts': '🚗', 'Braking': '🔴', 'Electrical': '⚡', 'Cooling': '🌡️', 'Engine': '⚙️'};
            return {
                id: p.sku,
                sku: p.sku,
                name: p.nameEn,
                nameAr: p.nameAr,
                brand: p.brand,
                cat: p.category,
                make: foundMake,
                models: p.compatibility,
                years: [2019, 2020, 2021, 2022],
                icon: catIcons[p.category] || '🔧',
                stock: p.stock > 10 ? 'in' : (p.stock > 0 ? 'low' : 'out'),
                isOE: p.isOE,
                isHot: p.isHot,
                isNew: p.isNew
            };
        });
        console.log("Mapped successfully:", allProducts.length);
    } catch (err) {
        console.error("Mapping failed:", err);
    }
}
testMap();
